func Convert(driver, db_type string, gotype string) string {
	if gotype != "" {
		return gotype
	}
	switch strings.ToLower(driver) {
	case "mysql":
		return MySQLConvert{}.Convert(db_type)
	}
	return ""
}

func ModuleName(table string) string {
	return Separate(Singular(strings.ToLower(table)), "_", ".")
}

func ClassName(table string) string {
	return Singular(Camel(table))
}

